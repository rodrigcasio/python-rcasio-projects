def square(number):
    """
    this f() returns the square of a given number
    """
    return number ** 2

def double(number):
    """
    this f() returns twice the value of a given number
    """
    return number * 2

def add(a, b):
    """
    this f() returns the sum of given numbers
    """
    return a + b
