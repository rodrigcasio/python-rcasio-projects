def square(number):
    """
    Returning the square of a given number
    """
    return number ** 2

def double(number):
    """
    Returning twice the value of a given number
    """
    return number * 2

def add(n1, n2):
    """
    Returning the sum of given numbers
    """
    return n1 + n2
